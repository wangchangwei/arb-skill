def lookup_legal_basis(keywords):
    if not keywords:
        return []
    scores = {}
    for i, basis in enumerate(_LABOR_LAW_DATABASE):
        score = 0
        kw_lower = [k.lower() for k in keywords]
        for tag in basis.apply_to:
            for kw in kw_lower:
                if kw in tag.lower():
                    score += 2
        for kw in kw_lower:
            if kw in basis.title.lower():
                score += 3
            if kw in basis.summary.lower():
                score += 1
        if score > 0:
            scores[i] = score
    sorted_indices = sorted(scores.keys(), key=lambda i: scores[i], reverse=True)
    return [_LABOR_LAW_DATABASE[i] for i in sorted_indices]


